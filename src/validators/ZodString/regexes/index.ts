export const dateRegexSource = `((\\d\\d[2468][048]|\\d\\d[13579][26]|\\d\\d0[48]|[02468][048]00|[13579][26]00)-02-29|\\d{4}-((0[13578]|1[02])-(0[1-9]|[12]\\d|3[01])|(0[469]|11)-(0[1-9]|[12]\\d|30)|(02)-(0[1-9]|1\\d|2[0-8])))`;
export const dateRegex = new RegExp(`^${dateRegexSource}$`);

export function timeRegexSource(args: { precision?: number | null }) {
    let regex = `([01]\\d|2[0-3]):[0-5]\\d:[0-5]\\d`;

    if (args.precision) {
        regex = `${regex}\\.\\d{${args.precision}}`;
    } else if (args.precision == null) {
        regex = `${regex}(\\.\\d+)?`;
    }
    return regex;
}

export function timeRegex(args: {
    offset?: boolean;
    local?: boolean;
    precision?: number | null;
}) {
    return new RegExp(`^${timeRegexSource(args)}$`);
}

export function datetimeRegex(args: {
    precision?: number | null;
    offset?: boolean;
    local?: boolean;
}) {
    let regex = `${dateRegexSource}T${timeRegexSource(args)}`;

    const opts: string[] = [];
    opts.push(args.local ? `Z?` : `Z`);
    if (args.offset) opts.push(`([+-]\\d{2}:?\\d{2})`);
    regex = `${regex}(${opts.join("|")})`;
    return new RegExp(`^${regex}$`);
}